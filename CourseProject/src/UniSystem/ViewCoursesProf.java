    private void populateFields() {
        try {
            txtCC.setText(rs.getString("course_code"));
            txtCN.setText(rs.getString("course_name"));
            txtCRN.setText(rs.getString("crn"));
            txtCreds.setText(rs.getString("credits"));
            txtTime.setText(rs.getString("time"));
            txtLoc.setText(rs.getString("location"));
            txtSem.setText(rs.getString("semester"));

            EnableDisableButtons();
        } catch (SQLException ex) {
            Logger.getLogger(ViewCoursesProf.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void MoveNext() {
        try {
            if (!rs.isLast()) {
                rs.next();
                populateFields();
            }
        } catch (SQLException ex) {
            Logger.getLogger(ViewCoursesProf.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        MoveNext();
    }//GEN-LAST:event_btnNextActionPerformed

    private void MovePrevious() {
        try {
            if (!rs.isFirst()) {
                rs.previous();
                populateFields();
            }
        } catch (SQLException ex) {
            Logger.getLogger(ViewCoursesProf.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void EnableDisableButtons() {
        try {
            if (rs.isFirst()) {
                btnPrevious.setEnabled(false);
            } else {
                btnPrevious.setEnabled(true);
            }
            if (rs.isLast()) {
                btnNext.setEnabled(false);
            } else {
                btnNext.setEnabled(true);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ViewCoursesProf.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
