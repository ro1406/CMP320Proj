    private void getNewData() {

        try {
            rs = statement.executeQuery("SELECT cs.Course_code,c.course_name,c.credits,cs.CRN,cs.semester,cs.location,cs.time "
                    + "FROM courses c,courses_sections cs WHERE cs.pid = "+currUserId+" AND c.course_code=cs.course_code ORDER BY cs.CRN ASC");
            
            // populate rest of fields
            rs.beforeFirst();
            rs.first();
            populateFields();
        } catch (SQLException e) {
            javax.swing.JLabel label = new javax.swing.JLabel("SQL Error - Display selected course.");
            label.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 18));
            JOptionPane.showMessageDialog(null, label, "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }

